function add(firstN,secondN){
	return firstN + secondN;
}

function subtract(firstN,secondN){
	return firstN - secondN;
}

function multiple(firstN,secondN){
	return firstN * secondN;
}

function divide(firstN,secondN){
	return firstN / secondN;
}

export{
	add,
	subtract,
	multiple,
	divide
};